
fp(x,y,p) = 2.0 * ((x^p + y^p)/2.0)^(1/p)

f_2(x, y) = 2 *((x^2 + y^2)/2)^(1/2)
f_5(x, y) = 2 *((x^5 + y^5)/2)^(1/5)
f_10(x, y) = 2 *((x^10 + y^10)/2)^(1/10)

f1(x,y) = 2.0 * sqrt(x * y)
f2(x,y) = x + y
f3(x,y) = 2.0 * sqrt((x^2 + y^2) / 2.0)
f4(x,y) = 2 * (1.0 / ((1.0 / x + 1.0 / y) / 2.0))
f5(x,y) = abs(x - y)
f_max(x, y) = 2.0 * max(x, y)
